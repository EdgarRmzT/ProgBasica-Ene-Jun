#Determinar si un numero es par, impar o multiplo de otro
num1= int(input("Introduzca un numero:"))
num2= int(input("Introduce otro numero para verificar si es multiplo"))

if num1 % 2==0:
    print("El", num1, "es par")
else:
    print("El", num1, "no es par")

if num1 % num2==0:
    print("El numero:", num1, "es multiplo de:", num2)
else:
    print("El numero:", num1, "no es multiplo de:", num2)

